class Calculadora:

    def sum(self, number_one, number_two):
        return number_one + number_two
    
    def subtraction(self, number_one, number_two):
        return number_one - number_two

    def multiplication(self, number_one, number_two):
        return number_one * number_two

    def division(self, number_one, number_two):
        return number_one / number_two